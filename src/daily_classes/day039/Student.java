/**
 * EXAMPLE CLASS
 * Any time you create a class, it will become a blueprint for an object
 * and this class type can be a type for a variable
 * for example String has its own class so we can create a String variable
 * and assign a String object
 *
 * in here Student becomes a blueprint for creating Student Object
 * and any Student object has 3 attributes defined by instance field|variable
 */
public class Student {

// instant field | variable :

    String name ;
    int age ;
    char gender ;

}
