package daily_classes.day006;

public class IncrementDecrementOperator {

    public static void main(String[] args) {

        int offerCount = 2;

        System.out.println("Initial offer count: " + offerCount);

        ++ offerCount;

        System.out.println("new offer count after ++ offerCount : " + offerCount);

        -- offerCount;

        System.out.println("new offer count after -- offerCount : " + offerCount);

    }
}
