package daily_classes.day022;

public class StringArray {

    public static void main(String[] args) {

        String[] family = new String [5];

        family[0] = "Kris";
        family[1] = "Cathey";
        family[2] = "Christin";
        family[3] = "Rich";
        family[4] = "Me";

        for (int i = 0; i < family.length -1; i++) {

            System.out.println(family[i]);
        }

    }
}
