package daily_classes.day007;

public class IncrementDecrementPractice {
    public static void main(String[] args) {


        int apple = 8;

        apple++;  // apple = 9

        System.out.println(apple); //printing already incremented value

//        System.out.println( ++apple); //incrementing by one BEFORE PRINTING "APPLE" (apple = 10)

        System.out.println(apple++);    //incrementing by one AFTER PRINTING "APPLE"  (OR apple = 9)


    }
}
